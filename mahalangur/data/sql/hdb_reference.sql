DROP TABLE IF EXISTS hdb_reference;
CREATE TABLE hdb_reference(
     EXPID      CHAR(9)      NOT NULL
    ,REFID      CHAR(2)      NOT NULL
    ,RYEAR      CHAR(4)
    ,RTYPE      INTEGER
    ,RJRNL      VARCHAR(4)
    ,RAUTHOR    VARCHAR(140)
    ,RTITLE     VARCHAR(210)
    ,RPUBLISHER VARCHAR(70)
    ,RPUBDATE   CHAR(4)
    ,RLANGUAGE  VARCHAR(30)
    ,RCITATION  VARCHAR(30)
    ,RYAK94     VARCHAR(5)
    ,RNOTES     TEXT
    ,PRIMARY KEY (EXPID ASC, REFID ASC)
) WITHOUT ROWID;